        <img class="image_beast" src="<?= ASSETS?>image/ainur.jpeg">
        <p>
        Os Ainur são seres espirituais criados por Eru Ilúvatar, o supremo criador na mitologia de J.R.R. Tolkien. Eles são considerados "pensamentos" de Ilúvatar, possuindo características únicas e uma parte da sabedoria do criador. Os Ainur são divididos em duas categorias principais: os Valar, que são os poderosos espíritos responsáveis pela formação e governança de Arda, e os Maiar, espíritos menores que servem os Valar e desempenham papéis diversos no mundo.<br><br>

        Antes da criação de Arda, os Ainur se reuniram para participar da "Música dos Ainur", um ato colaborativo em que cada um expressou sua essência através de melodias. Esta música foi fundamental na criação do universo, mas Melkor, um dos Ainur, introduziu dissonâncias ao tentar impor sua própria vontade. Esse conflito na música simboliza a luta entre o bem e o mal, que se refletirá nas histórias posteriores de Arda.<br><br>

        Após a música, os Valar desceram a Arda para moldar o mundo conforme a visão que haviam visto. Eles trabalharam juntos para criar montanhas, mares e florestas, estabelecendo a ordem no novo mundo.<br><br>

        Os Ainur não apenas representam a criação, mas também os temas de livre-arbítrio e a eterna luta entre luz e escuridão. Sua música e ações ilustram a complexidade da existência, destacando a beleza da harmonia e os desafios que surgem na busca por equilíbrio e ordem. Assim, os Ainur são fundamentais na cosmologia de Tolkien, refletindo a interação entre a vontade divina e a autonomia das criaturas.<br><br>
        </p>
