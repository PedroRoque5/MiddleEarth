        <h2 class="bestiario">Valar</h2>
        <img class="image_beast" src="<?= ASSETS?>image/Morgoth.webp">
        <p>
        Os Valar são doze em número, cada um associado a diferentes aspectos da criação e da natureza. Por exemplo, Manwë é o líder dos Valar e senhor dos ventos e das aves, enquanto Varda, sua esposa, é a rainha e a senhora das estrelas, responsável por iluminar o céu com suas criações. Aulë, o ferreiro, é o mestre da terra e dos minerais, enquanto Yavanna cuida das plantas e da vida natural. Outros Valar, como Mandos, que preside o destino dos mortos, e Ulmo, o senhor das águas, desempenham papéis essenciais na ordem do mundo.<br><br>

        Os Valar habitam Aman, uma terra abençoada além do mar, onde podem observar e intervir na vida dos povos de Arda. Sua presença é sentida, mas muitas vezes não é diretamente visível. Em momentos de grande necessidade, como durante as guerras contra Morgoth—um Valar corrompido que se tornou o maior antagonista do universo de Tolkien—eles descem a Arda para guiar e ajudar os Elfos e Homens.<br><br>

        Os Valar também têm um profundo amor e cuidado pela criação, e sua história é marcada por tentativas de ensinar e proteger os filhos de Ilúvatar: os Elfos e os Homens. No entanto, suas intervenções são sempre equilibradas por um respeito pela liberdade desses seres, que devem escolher seu próprio caminho.<br><br>

        Assim, os Valar representam não apenas a força e a autoridade, mas também a sabedoria e a compaixão, simbolizando a eterna luta entre a luz e a escuridão, a ordem e o caos no mundo criado por Tolkien. Através deles, somos lembrados da importância da esperança, da amizade e do sacrifício na busca por um mundo melhor.<br><br>
