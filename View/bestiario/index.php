        <h2 class="bestiario">Elfos</h2>
        <img class="image_beast" src="<?= ASSETS?>image/galadriel.jpg">
        <p>
        Na vasta e mágica Terra-média de J.R.R. Tolkien, os elfos surgem como seres etéreos, uma mistura de graça e sabedoria, moldados pela luz das estrelas. Com sua beleza sobrenatural, possuem traços delicados: pele pálida, olhos cintilantes que refletem a profundidade do céu noturno e cabelos que variam do dourado ao prateado. Suas orelhas pontudas são um símbolo de sua herança ancestral, conferindo-lhes uma aparência distinta e quase divina.<br><br>

        Habitantes das florestas e montanhas, os elfos são íntimos da natureza, capazes de se comunicar com as árvores e os animais. Suas vozes soam como melodias suaves, ecoando em canções que falam de tempos antigos e histórias esquecidas. Os elfos são imortais, mas não estão isentos da dor; carregam o peso de memórias longínquas e das perdas que moldaram suas vidas, especialmente em relação à queda de seu amado reino de Gondolin e à dor da separação de seus irmãos.<br><br>

        Artífices habilidosos, eles criam obras de arte e artefatos de beleza incomparável, como as famosas joias de Silmaril. Suas habilidades mágicas e seu conhecimento profundo das artes e da música os tornam guardiões do legado cultural da Terra-média. No entanto, sua sabedoria também traz um senso de melancolia, uma consciência do tempo que flui e do inevitável declínio que afeta até mesmo os seres imortais.<br><br>

        No final, os elfos são seres de luz em um mundo frequentemente tomado pela escuridão. Sua presença é um lembrete de um passado glorioso e um futuro de esperança, onde ainda brilha a possibilidade de redenção e beleza, mesmo nas horas mais sombrias. Eles são, em essência, a personificação da alma da Terra-média: pura, complexa e eternamente encantadora.<br><br>
